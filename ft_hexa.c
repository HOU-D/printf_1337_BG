#include "ft_printf.h"


int	ft_hexa(unsigned int n)
{
	char	*b;
	int  l;

	b = "0123456789ABCDEF";
	l = 0;
	if (n >= 16)
		l += ft_hexa (n / 16);
	l += ft_putchar (b[n % 16]);
	return (l);
}